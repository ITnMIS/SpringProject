package model;

public class GymFeeModel {

	private int G_F_Seq;
	private int Gidx;
	private String Period;
	private String Fee;
	private String SubFee;
	
	public int getG_F_Seq() {
		return G_F_Seq;
	}
	public void setG_F_Seq(int g_F_Seq) {
		G_F_Seq = g_F_Seq;
	}
	public int getGYM_Seq() {
		return Gidx;
	}
	public void setGYM_Seq(int gidx) {
		Gidx = gidx;
	}
	public String getPeriod() {
		return Period;
	}
	public void setPeriod(String period) {
		Period = period;
	}
	public String getFee() {
		return Fee;
	}
	public void setFee(String fee) {
		Fee = fee;
	}
	public String getSubFee() {
		return SubFee;
	}
	public void setSubFee(String subFee) {
		SubFee = subFee;
	}
	

	
}
